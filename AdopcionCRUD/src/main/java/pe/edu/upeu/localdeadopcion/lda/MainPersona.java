    private void slarkAdoptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_slarkAdoptarActionPerformed
        // TODO add your handling code here:
        Persona to = new Persona();
        to.setDni(ursaDni.getText());
        to.setNombre(ursaNombre.getText());
        to.setDireccion(ursaDireccion.getText());
        to.setIdentidad(invoMascotaAdoptar.getSelectedItem() == null ? ""
                : invoMascotaAdoptar.getSelectedItem().toString());
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            try {
                Persona resultado = personaService.updateEntidad(to);
                if (resultado != null) {
                    model = (DefaultTableModel) jTable1.getModel();
                    Object nuevo[] = {fila + 1, to.getDni(), to.getNombre(),
                        to.getIdentidad(), to.getDireccion()};
                    model.removeRow(fila);
                    model.insertRow(fila, nuevo);
                    resetForm();
                    JOptionPane.showMessageDialog(this, "modificado");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            try {

                if (JOptionPane.showConfirmDialog(this, "Esta seguro de crear un nuevo adopcion?", "Mensaje de confirmación", JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.INFORMATION_MESSAGE) == 0) {
                    if (personaService.saveEntidad(to) != false) {
                        model = (DefaultTableModel) jTable1.getModel();
                        Object nuevo[] = {model.getRowCount() + 1, to.getDni(),
                            to.getNombre(), to.getIdentidad(), to.getDireccion()};
                        model.addRow(nuevo);
                        resetForm();

                    }
                }
            } catch (Exception e) {
                
                System.err.println("Crear adopcion");
                
            }
        }

    }//GEN-LAST:event_slarkAdoptarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRowCount() > 0) {
            try {
                model = (DefaultTableModel) jTable1.getModel();
                int rowx = jTable1.getSelectedRow();
                Object valor = jTable1.getValueAt(rowx, 1);

                if (JOptionPane.showConfirmDialog(this,
                        "Esta seguro de eliminar este registrtro DNI:" + valor + " ? ", "  Mensaje de confirmación", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
                    model.removeRow(rowx);
                    personaService.deleteRegistEntdad(valor.toString());
                    resetForm();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una MASCOTA");
        }


    }//GEN-LAST:event_EliminarActionPerformed

