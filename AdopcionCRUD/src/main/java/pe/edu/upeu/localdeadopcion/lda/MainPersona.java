    public void listarPersonas() {
        List<Persona> listarPersonas = personaService.listarEntidad();
        if (listarPersonas != null) {
            jTable1.setAutoCreateRowSorter(true);
            model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            Object[] ob = new Object[5];
            for (int i = 0; i < listarPersonas.size(); i++) {
                ob[0] = i + 1;
                ob[1] = listarPersonas.get(i).getDni();
                ob[2] = listarPersonas.get(i).getNombre();
                ob[3] = listarPersonas.get(i).getIdentidad();
                ob[4] = listarPersonas.get(i).getDireccion();
                model.addRow(ob);
            }
        }
        jTable1.setModel(model);
    }

    public void resetForm() {
        ursaDni.setText("");
        ursaNombre.setText("");
        ursaDireccion.setText("");
        invoMascotaAdoptar.setSelectedIndex(0);
        ursaDni.requestFocus();
    }

    private void paintForm() {
        if (jTable1.getSelectedRow() != -1) {
            model = (DefaultTableModel) jTable1.getModel();
            int rowx = jTable1.getSelectedRow();
            Object valor = jTable1.getValueAt(rowx, 1);
            Persona d = personaService.buscarCliente(valor.toString());
            ursaDni.setText(d.getDni());
            ursaNombre.setText(d.getNombre());
            ursaDireccion.setText(d.getDireccion());
            invoMascotaAdoptar.setSelectedItem(d.getIdentidad());
            ursaDni.setEditable(false);
            slarkAdoptar.setText("modificar");
        } else {
            ursaDni.setEditable(true);
        }
    }

